namespace ScholarRescue.Models.Enums
{
    /// <summary>
    /// Represents the academic level required for an order.
    /// </summary>
    public enum AcademicLevel
    {
        /// <summary>
        /// High school level academic work.
        /// </summary>
        HighSchool = 0,

        /// <summary>
        /// College/community college level work.
        /// </summary>
        College = 1,

        /// <summary>
        /// Undergraduate university level work.
        /// </summary>
        Undergraduate = 2,

        /// <summary>
        /// Master's degree level work.
        /// </summary>
        Masters = 3,

        /// <summary>
        /// Doctoral/PhD level work.
        /// </summary>
        PhD = 4
    }
}